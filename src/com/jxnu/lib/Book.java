package com.jxnu.lib;


public class Book{
	private int Book_Id;
	private String Book_Name;
	private String Author;
	private String Types_Of_Book;
	private int Borrowd;
	private String Date_Issued;
	private String Book_Info;
	private int Price;
	private String Book_Img;
	public int getBook_Id() {
		return Book_Id;
	}
	public void setBook_Id(int book_Id) {
		Book_Id = book_Id;
	}
	public String getBook_Name() {
		return Book_Name;
	}
	public void setBook_Name(String book_Name) {
		Book_Name = book_Name;
	}
	public String getAuthor() {
		return Author;
	}
	public void setAuthor(String author) {
		Author = author;
	}
	public String getTypes_Of_Book() {
		return Types_Of_Book;
	}
	public void setTypes_Of_Book(String types_Of_Book) {
		Types_Of_Book = types_Of_Book;
	}
	public int getBorrowd() {
		return Borrowd;
	}
	public void setBorrowd(int borrowd) {
		Borrowd = borrowd;
	}
	public String getDate_Issued() {
		return Date_Issued;
	}
	public void setDate_Issued(String date_Issued) {
		Date_Issued = date_Issued;
	}
	public String getBook_Info() {
		return Book_Info;
	}
	public void setBook_Info(String book_Info) {
		Book_Info = book_Info;
	}
	public int getPrice() {
		return Price;
	}
	public void setPrice(int price) {
		Price = price;
	}
	public String getBook_Img() {
		return Book_Img;
	}
	public void setBook_Img(String book_Img) {
		Book_Img = book_Img;
	}
	
	
	

}
